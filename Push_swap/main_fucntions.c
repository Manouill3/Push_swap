int	count_word(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while(str[i])
	{
		if (str[i] > 32 && str[i] < 123)
		{
			if (str[i + 1] < 33 || str[i + 1] == '\0')
				count++;
		}
		i++;
	}
	return (count);
}
